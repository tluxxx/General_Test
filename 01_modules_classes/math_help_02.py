# some mathematical functions

import math

def exponential(x):
  return math.exp(x)

def sinus(x):
  return math.sin(x*math.pi)

def root(x):
  return math.sqrt(x)

def square(x):
  return x**2

def kehr(x):
  return 1/x-1

def tangens(x):
  return math.tan(x*math.pi)

def cubic(x):
  return x**3

def cosinus(x):
  return math.cos(x*math.pi)